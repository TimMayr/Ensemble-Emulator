use crate::emulation::emu::{Console, Consoles};
use crate::emulation::nes::Nes;
use crate::frontend::Frontends;

#[test]
fn test_09_even_odd_frames() {
    let mut emu = Consoles::Nes(Nes::default());
    emu.load_rom(&String::from(
        "./tests/nes-test-roms/ppu_vbl_nmi/rom_singles/09-even_odd_frames.nes",
    ));
    emu.power();
    emu.run_until(36342973).expect("Error while running test");

    let whole_mem = emu.get_memory_debug(Some(0x6000..=0x602D));
    let cpu_mem = whole_mem[0].as_slice();

    let expected = [
        0x00, 0xDE, 0xB0, 0x61, 0x30, 0x30, 0x20, 0x30, 0x31, 0x20, 0x30, 0x31, 0x20, 0x30, 0x32,
        0x20, 0x0A, 0x30, 0x39, 0x2D, 0x65, 0x76, 0x65, 0x6E, 0x5F, 0x6F, 0x64, 0x64, 0x5F, 0x66,
        0x72, 0x61, 0x6D, 0x65, 0x73, 0x0A, 0x0A, 0x50, 0x61, 0x73, 0x73, 0x65, 0x64, 0x0A, 0x00,
    ];

    assert_eq!(cpu_mem[0], 0);
    assert_eq!(&cpu_mem[..expected.len()], &expected);
}
