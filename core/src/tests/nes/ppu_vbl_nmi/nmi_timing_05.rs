use crate::emulation::emu::{Console, Consoles};
use crate::emulation::nes::Nes;
use crate::frontend::Frontends;

#[test]
fn test_05_nmi_timing() {
    let mut emu = Consoles::Nes(Nes::default());
    emu.load_rom(&String::from(
        "./tests/nes-test-roms/ppu_vbl_nmi/rom_singles/05-nmi_timing.nes",
    ));
    emu.reset();
    emu.run_until(&mut Frontends::default(), 105341973)
        .expect("Error while running test");

    let whole_mem = emu.get_memory_debug(Some(0x6000..=0x604D));
    let cpu_mem = whole_mem[0].as_slice();

    let expected = [
        0x00, 0xDE, 0xB0, 0x61, 0x30, 0x30, 0x20, 0x34, 0x0A, 0x30, 0x31, 0x20, 0x34, 0x0A, 0x30,
        0x32, 0x20, 0x34, 0x0A, 0x30, 0x33, 0x20, 0x33, 0x0A, 0x30, 0x34, 0x20, 0x33, 0x0A, 0x30,
        0x35, 0x20, 0x33, 0x0A, 0x30, 0x36, 0x20, 0x33, 0x0A, 0x30, 0x37, 0x20, 0x33, 0x0A, 0x30,
        0x38, 0x20, 0x33, 0x0A, 0x30, 0x39, 0x20, 0x32, 0x0A, 0x0A, 0x30, 0x35, 0x2D, 0x6E, 0x6D,
        0x69, 0x5F, 0x74, 0x69, 0x6D, 0x69, 0x6E, 0x67, 0x0A, 0x0A, 0x50, 0x61, 0x73, 0x73, 0x65,
        0x64, 0x0A, 0x00,
    ];

    assert_eq!(cpu_mem[0], 0);
    assert_eq!(&cpu_mem[..expected.len()], &expected);
}
